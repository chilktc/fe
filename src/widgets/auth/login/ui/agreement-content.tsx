interface AgreementContentViewProps {
  contentKey: AgreementContentKey;
}

export function AgreementContentView({
  contentKey,
}: AgreementContentViewProps): ReactNode {
  const content = AGREEMENT_CONTENTS[contentKey];

  return (
    <div className="space-y-5 text-body-6 text-gray-800">
      {content.intro.map((paragraph) => (
        <p key={paragraph}>{paragraph}</p>
      ))}

      {content.sections.map((section) => (
        <section key={section.heading} className="space-y-2">
          <h3 className="font-semibold text-gray-900">{section.heading}</h3>
          {section.body?.map((paragraph) => <p key={paragraph}>{paragraph}</p>)}
          {section.items && (
            <ul className="list-disc space-y-1 pl-5">
              {section.items.map((item) => (
                <li key={item}>{item}</li>
              ))}
            </ul>
          )}
        </section>
      ))}

      {content.footer?.map((paragraph) => (
        <p key={paragraph}>{paragraph}</p>
      ))}
    </div>
  );
}
